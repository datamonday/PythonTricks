# 1. 编写程序，生成包含1000个0到100之间的随机整数，并统计每个元素的出现次数。

import random

rand_list = [random.randint(0, 100) for i in range(1000)]

unique_rand_list = set(rand_list)
unique_dict = {}
for i in unique_rand_list:
    k = 0
    for j in rand_list:
        if j == i:
            k += 1
    unique_dict[i] = k
    
unique_dict

